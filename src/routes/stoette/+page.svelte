            <h2 class="text-2xl pt-4">Økonomisk støtte</h2>
            <p class="pt-2">
                Alle studenterinitierede aktiviteter på SCIENCE har mulighed for at søge penge fra SCIENCEpuljen. <a class="text-blue-700" target="_blank" href="https://sciencepuljen.dk">Læs mere og ansøg på deres hjemmeside</a>.
            </p>
            <p class="pt-2">
                Ligeledes uddeler Studenterrådet aktivitetsstøtte, som kan søges bredt af alle studerende på KU. <a class="text-blue-700" target="_blank" href="https://www.srku.dk/nyheder/aktivitet">Læs mere og ansøg på deres hjemmeside</a>.
            </p>
            <p class="pt-2">
                Som studerende på DIKU kan du søge økonomisk støtte direkte fra instituttet til aktiviteter, der er målrettet DIKU-studerende. <a class="text-blue-700" target="_blank" href="https://kunet.ku.dk/studie/datalogi-ba/Sider/Emne.aspx?topic=St%C3%B8tte%20til%20arrangementer&topicId=1d211893-f91e-4624-9f77-7a7128269636">Læs mere og ansøg gennem KUnet</a>.
            </p>
            <p class="pt-2">
                Derudover har du altid mulighed for at søge diverse fagforeninger og virksomheder om sponsorater. Især inden for IT-verdenen findes der mange, som rigtig gerne vil støtte studerendes arrangementer.
            </p>
            <h2 class="text-2xl pt-4">Lokalebooking</h2>
            <p class="pt-2">
                På SCIENCE kan du booke lokaler til både faglige og sociale arrangementer, hvor der dog bl.a. kan være krav om betaling for ekstern rengøring til større arrangementer. <a class="text-blue-700" target="_blank" href="https://kunet.ku.dk/studie/datalogi-ba/Sider/Emne.aspx?topic=Caf%C3%A9er%20og%20fester&topicId=0ccd565d-bf57-48a7-804a-11ea4f288eb6#140806_noPictureOnTop_content">Læs mere om reglerne og udfyld bestillingsformularen gennem KUnet</a>. Skriv til lokaler-noerre@adm.ku.dk, hvis du har spørgsmål om booking af lokaler på Nørre Campus.
            </p>
            <p class="pt-2">
                Derudover er det muligt at låne lokaler på campus direkte gennem forskellige foreninger som <a class="text-blue-700" target="_blank" href="https://kantinen.org/laan_kantinen">Kantinen på DIKU</a> og <a class="text-blue-700" target="_blank" href="https://cafeen.org/lej-cafeen">Caféen?</a>.
            </p>
            <h2 class="text-2xl pt-4">PR</h2>
            <p class="pt-2">
                For at skabe opmærksomhed omkring ens arrangement er det ofte en god idé både at hænge fysiske plakater op på campus, skrive ud i DIKU-årgangenes Facebook-grupper og evt. spørge om lov til at lave reklame til forelæsninger eller lignende. Til at hjælpe med denne proces tilbyder DIKU Fagråd at indkøbe et rollup-banner med print om netop din forening eller dit særlige arrangement. Kontakt os, hvis du er interesseret i denne mulighed.
            </p>
            <h2 class="text-2xl pt-4">Spørgsmål</h2>
            <p class="pt-2">
